/**
 * Bst entity node.
 */
public class BstEntityNode {

    /**
     * id.
     */
    private Long id;

    /**
     * Creates new BstEntityNode.
     *
     * @param id
     */
    public BstEntityNode(Long id) {
        this.id = id;
    }

     /**
     * Get id.
     *
     * @return id
     */
    public Long getId() {
        return id;
    }
}
