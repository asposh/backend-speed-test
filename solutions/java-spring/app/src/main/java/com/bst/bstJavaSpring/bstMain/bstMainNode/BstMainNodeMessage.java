package com.bst.bstJavaSpring.bstMain.bstMainNode;

/**
 * Bst main message node.
 */
public class BstMainNodeMessage {

    /**
     * message.
     */
    private String message;

    /**
     * Creates new BstMainNodeMessage.
     *
     * @param message
     */
    public BstMainNodeMessage(String message) {
        this.message = message;
    }

    /**
     * Get message.
     *
     * @return message
     */
    public String getMessage() {
        return message;
    }
}
